package assignment5;

public class IllegalDamageException extends Exception {

	public IllegalDamageException() {
		super();
	}

	public IllegalDamageException(String arg0) {
		super(arg0);
	}

	public IllegalDamageException(Throwable arg0) {
		super(arg0);
	}

	public IllegalDamageException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public IllegalDamageException(String arg0, Throwable arg1, boolean arg2, boolean arg3) {
		super(arg0, arg1, arg2, arg3);
	}

}
